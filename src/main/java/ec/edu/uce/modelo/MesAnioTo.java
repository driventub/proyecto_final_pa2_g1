package ec.edu.uce.modelo;

public class MesAnioTo {
    
    private String mes;
    private String anio;

    public MesAnioTo(String mes, String anio) {
        this.mes = mes;
        this.anio = anio;
    }

    
    
    public MesAnioTo() {
    }



    public String getMes() {
        return mes;
    }
    public void setMes(String mes) {
        this.mes = mes;
    }
    public String getAnio() {
        return anio;
    }
    public void setAnio(String anio) {
        this.anio = anio;
    }

    

}
